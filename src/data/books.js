// Helper functions
export const getBookById = (id) => {
  return booksData.find(book => book.id === id);
};

export const getBestsellers = () => {
  return booksData.filter(book => book.isBestseller);
};

export const getNewArrivals = () => {
  return booksData.filter(book => book.isNew);
};

export const getBooksByCategory = (category) => {
  return booksData.filter(book => 
    book.category.toLowerCase() === category.toLowerCase()
  );
};

export const searchBooks = (query) => {
  const searchTerm = query.toLowerCase();
  return booksData.filter(book =>
    book.title.toLowerCase().includes(searchTerm) ||
    book.author.toLowerCase().includes(searchTerm) ||
    book.category.toLowerCase().includes(searchTerm) ||
    book.description.toLowerCase().includes(searchTerm)
  );
};

export const getRelatedBooks = (bookId, category, limit = 4) => {
  return booksData
    .filter(book => book.id !== bookId && book.category === category)
    .slice(0, limit);
};

export const getCategories = () => {
  const categories = [...new Set(booksData.map(book => book.category))];
  return categories.sort();
};

export const getAuthors = () => {
  const authors = [...new Set(booksData.map(book => book.author))];
  return authors.sort();
};

export const getFeaturedBooks = (limit = 8) => {
  return booksData
    .filter(book => book.isBestseller || book.isNew)
    .slice(0, limit);
};